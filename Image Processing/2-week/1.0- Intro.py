print(type(7))        # => int
print(type(-7))       # => int
print(type(7.5))      # => float
print(type(-7.5))     # => float

my_complexNumber = 5+6j
print(type(my_complexNumber))                                   # => complex
print("Real Part Is : {}".format(my_complexNumber.real))        # => 5.0
print("Imaginary Part Is : {}".format(my_complexNumber.imag))   # => 6.0



